#!/usr/bin/env node
/**
 * Builds competition async-v2 team fork from bundled MVP v2.0-async.
 *
 * Usage: yarn usercase:build-competition-async-v2 <team>
 */
import { cpSync, existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { spawnSync } from 'node:child_process';

import { finalizeUserCaseBuild } from './lib/usercase-post-build.mjs';

const SPRINT_ID = 'comp-mvp-async-v2-2026-06-25';
const COMPETITION_BASE = 'v2.0-async';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const scriptsDir = join(root, 'docs/device-board-scripts');
const mvpDir = join(scriptsDir, 'usercase-mvp-microphone');

/** @type {readonly string[]} */
const TEAMS = ['alpha', 'beta', 'gamma'];

const EXPORT_NAMES = {
  alpha: 'DEFAULT_USERCASE_MVP_MICROPHONE_ALPHA_ASYNC_V2_DOCUMENT',
  beta: 'DEFAULT_USERCASE_MVP_MICROPHONE_BETA_ASYNC_V2_DOCUMENT',
  gamma: 'DEFAULT_USERCASE_MVP_MICROPHONE_GAMMA_ASYNC_V2_DOCUMENT',
};

/** @param {string} team */
function teamUserCaseId(team) {
  return `usercase-mvp-microphone-${team}-async-v2`;
}

/** @param {string} team */
function teamEmbeddedRelative(team) {
  return `packages/device-board/src/graph/default-usercase-mvp-microphone-${team}-async-v2.generated.ts`;
}

/** @param {string} team */
function teamOutDir(team) {
  return join(scriptsDir, teamUserCaseId(team));
}

async function loadDeviceBoardPackModule() {
  const build = spawnSync('yarn', ['workspace', '@membrana/device-board', 'build'], {
    cwd: root,
    stdio: 'inherit',
    shell: true,
  });
  if (build.status !== 0) {
    throw new Error('device-board build failed before competition pack');
  }

  const packUrl = pathToFileURL(
    join(root, 'packages/device-board/dist/graph/usercase-competition-pack.js'),
  ).href;
  const mvpUrl = pathToFileURL(
    join(root, 'packages/device-board/dist/graph/default-usercase-mvp-microphone.js'),
  ).href;

  const [packMod, mvpMod] = await Promise.all([import(packUrl), import(mvpUrl)]);
  return {
    packMvpUserCaseForTeam: packMod.packMvpUserCaseForTeam,
    getDefaultMvpMicrophoneDocument: mvpMod.getDefaultMvpMicrophoneDocument,
  };
}

/** @param {string} team @param {Record<string, unknown>} document */
function writeEmbeddedDocument(team, document) {
  const abs = join(root, teamEmbeddedRelative(team));
  const exportName = EXPORT_NAMES[team];
  const body = JSON.stringify(document, null, 2);
  writeFileSync(
    abs,
    `/**
 * AUTO-GENERATED by scripts/build-usercase-competition-async-v2-team.mjs — do not edit.
 */
import type { DeviceScenarioDocument } from '@membrana/core';

export const ${exportName} = ${body} as unknown as DeviceScenarioDocument;
`,
    'utf8',
  );
}

/** @param {string} team */
function seedTeamBundleFromMvp(team) {
  const outDir = teamOutDir(team);
  mkdirSync(outDir, { recursive: true });

  const mvpManifest = JSON.parse(readFileSync(join(mvpDir, 'manifest.json'), 'utf8'));
  const userCaseId = teamUserCaseId(team);

  /** @type {Record<string, { title: string, description: string }>} */
  const teamCopy = {
    alpha: {
      title: 'Alpha: Live Observation Pipeline (async v2)',
      description:
        'Competition async v2 Team Alpha — operator journey + async narrative (upload/detached drone).',
    },
    beta: {
      title: 'Beta: Measured modular UserCase (async v2)',
      description: 'Competition async v2 Team Beta — modular async functions, verify-layout metrics.',
    },
    gamma: {
      title: 'Gamma: Poster UserCase (async v2)',
      description: 'Competition async v2 Team Gamma — poster ①–⑥ with async upload step.',
    },
  };

  const manifest = {
    ...mvpManifest,
    id: userCaseId,
    title: teamCopy[team].title,
    description: teamCopy[team].description,
    tier: 'community',
    commentGroupProfile: team,
    packProfile: team,
    embeddedDocument: teamEmbeddedRelative(team),
    competitionSprint: SPRINT_ID,
    competitionBase: COMPETITION_BASE,
    branches: {},
  };

  for (const [branch, meta] of Object.entries(mvpManifest.branches ?? {})) {
    const { legacyFile: _legacy, ...branchMeta } = meta;
    manifest.branches[branch] = { ...branchMeta };
  }

  for (const file of readdirSync(mvpDir).filter((name) => name.endsWith('.json'))) {
    if (file === 'manifest.json') {
      continue;
    }
    const dest = join(outDir, file);
    cpSync(join(mvpDir, file), dest);
    const doc = JSON.parse(readFileSync(dest, 'utf8'));
    writeFileSync(
      dest,
      `${JSON.stringify(
        {
          ...doc,
          scenarioTitle: doc.scenarioTitle?.replace(
            'UserCase MVP microphone',
            `UserCase ${teamCopy[team].title}`,
          ),
          userCaseId,
          exportedAt: new Date().toISOString(),
        },
        null,
        2,
      )}\n`,
      'utf8',
    );
  }

  writeFileSync(join(outDir, 'manifest.json'), `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');
}

const teamArg = process.argv[2];
if (teamArg === undefined || !TEAMS.includes(teamArg)) {
  console.error(
    `Usage: node scripts/build-usercase-competition-async-v2-team.mjs <${TEAMS.join('|')}>`,
  );
  process.exit(1);
}

if (!existsSync(mvpDir)) {
  console.error(
    `MVP UserCase folder missing: ${mvpDir}. Run yarn usercase:build-mvp-microphone first.`,
  );
  process.exit(1);
}

try {
  seedTeamBundleFromMvp(teamArg);
  const { packMvpUserCaseForTeam, getDefaultMvpMicrophoneDocument } = await loadDeviceBoardPackModule();
  const packed = packMvpUserCaseForTeam(teamArg, getDefaultMvpMicrophoneDocument());
  writeEmbeddedDocument(teamArg, packed);
  console.log(`Packed ${teamUserCaseId(teamArg)} → ${teamEmbeddedRelative(teamArg)}`);

  await finalizeUserCaseBuild(teamUserCaseId(teamArg));
  console.log(`Built ${teamUserCaseId(teamArg)} (${COMPETITION_BASE}) with layout canon + verify`);
} catch (error) {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
}
