#!/usr/bin/env node
/**
 * Publish closed competition UserCase forks to device-board catalog picker (community tier).
 *
 * Usage:
 *   yarn comp:publish-catalog --id comp-mvp-async-v2-2026-06-25
 *   yarn comp:publish-catalog --id comp-mvp-async-v2-2026-06-25 --dry-run
 *   yarn comp:publish-catalog --id comp-mvp-async-v2-2026-06-25 --skip-verify
 *
 * Manifest: docs/competition-sprint/<sprint-id>/CATALOG_PUBLISH.json
 * Output: packages/device-board/src/catalog/community-competition-user-case-entries.ts
 * Canon: docs/prompts/COMPETITION_CATALOG_PUBLISH_REGULATION.md
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT_REL = 'packages/device-board/src/catalog/community-competition-user-case-entries.ts';

const args = process.argv.slice(2);
const idIdx = args.indexOf('--id');
const sprintId = idIdx >= 0 ? args[idIdx + 1] : undefined;
const dryRun = args.includes('--dry-run');
const skipVerify = args.includes('--skip-verify');

if (!sprintId) {
  console.error(
    'Usage: yarn comp:publish-catalog --id <sprint-id> [--dry-run] [--skip-verify]',
  );
  process.exit(1);
}

const manifestPath = join(root, 'docs/competition-sprint', sprintId, 'CATALOG_PUBLISH.json');
if (!existsSync(manifestPath)) {
  console.error(`CATALOG_PUBLISH.json not found: ${manifestPath}`);
  process.exit(1);
}

/** @type {{ sprintId: string; winner?: string; verifyCommand: string; entries: Array<{ id: string; loader: string; loaderModule: string; title: string; description: string; functionCount: number }> }} */
const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));

if (!Array.isArray(manifest.entries) || manifest.entries.length === 0) {
  console.error('CATALOG_PUBLISH.json: entries[] required');
  process.exit(1);
}

if (!skipVerify) {
  const [cmd, ...cmdArgs] = manifest.verifyCommand.split(/\s+/);
  console.error(`Verify: ${manifest.verifyCommand}`);
  const verify = spawnSync(cmd, cmdArgs, { cwd: root, stdio: 'inherit', shell: true });
  if (verify.status !== 0) {
    console.error('Verify failed — fix packs or use --skip-verify (not for CI)');
    process.exit(verify.status ?? 1);
  }
}

const importLines = manifest.entries.map((entry) => {
  const base = entry.loaderModule.replace(/\.js$/, '');
  return `import { ${entry.loader} } from '../graph/${base}.js';`;
});

const entryBlocks = manifest.entries.map((entry) => {
  const winnerNote =
    manifest.winner !== undefined &&
    entry.id.includes(`-${manifest.winner}-`) &&
    !entry.title.includes('winner')
      ? ' · winner'
      : '';
  const title = entry.title.includes('winner') ? entry.title : `${entry.title}${winnerNote}`;
  return `  {
    id: '${entry.id}',
    title: '${title.replace(/'/g, "\\'")}',
    description: '${entry.description.replace(/'/g, "\\'")}',
    deviceKind: 'microphone',
    tier: 'community',
    layoutProfile: 'exec-lr-v1',
    branchCount: 6,
    functionCount: ${entry.functionCount},
    loadDocument: ${entry.loader},
  }`;
});

const ts = `/**
 * AUTO-GENERATED by yarn comp:publish-catalog — do not edit by hand.
 * Sprint: \`${manifest.sprintId}\`
 * Regenerate: yarn comp:publish-catalog --id ${manifest.sprintId}
 */
import type { UserCaseCatalogEntry } from './user-case-catalog-types.js';
${importLines.join('\n')}

/** Active competition forks in device-board UserCase picker (\`tier: community\`). */
export const COMMUNITY_COMPETITION_USER_CASE_ENTRIES: readonly UserCaseCatalogEntry[] = [
${entryBlocks.join(',\n')},
];
`;

const statePath = join(root, 'docs/competition-sprint', sprintId, 'CATALOG_PUBLISH_STATE.md');
const stateMd = `# Catalog publish state — ${sprintId}

| Поле | Значение |
|------|----------|
| **publishedAt** | ${new Date().toISOString().slice(0, 10)} |
| **picker entries** | ${manifest.entries.length} |
| **verify** | \`${manifest.verifyCommand}\` |
| **output** | \`${OUT_REL}\` |

## IDs in picker

${manifest.entries.map((e) => `- \`${e.id}\``).join('\n')}

Operator: Apply from device-board UserCase list → Run → \`yarn logs:parse\`.
`;

if (dryRun) {
  console.log(ts);
  console.error(`\n[dry-run] Would write ${OUT_REL} and ${statePath}`);
  process.exit(0);
}

writeFileSync(join(root, OUT_REL), ts, 'utf8');
writeFileSync(statePath, stateMd, 'utf8');
console.error(`Wrote ${OUT_REL}`);
console.error(`Wrote ${statePath}`);
console.error(`\nNext: update catalog tests, run yarn workspace @membrana/device-board test`);
