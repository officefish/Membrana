import assert from 'node:assert/strict';
import test from 'node:test';

import { meetingVerdictProblems } from './lib/protocol-validator.mjs';

/**
 * Минимальный протокол. ВАЖНО: `protocolBody` отсчитывает тело от первой реплики
 * `[Роль]:` — без неё тело пусто и любая проверка сработает по ложной причине
 * (поймано на первом же прогоне этих тестов).
 */
const head = '# Метаданные сеанса\n\n| Поле | Значение |\n\n---\n\n[Музыкант]: Начали.\n\n';

test('годный вердикт: посылки — входы, DoD без чужих ID', () => {
  const md = `${head}[Teamlead]: Фиксирую.

## Итоговое решение консилиума

**Полный список посылок вердикта:**
1. Сигнатура из M1 в силе.
2. Инвариант I3 (дедуп по origin-hash) в силе.

**Definition of Done:**
- Спецификация фиксирует сигнатуру и тест без сети.
`;
  assert.deepEqual(meetingVerdictProblems(md, ['H', 'F', 'T']), []);
});

test('ловит ВЫВОД в списке посылок (рецидив M1′)', () => {
  const md = `${head}## Итоговое решение консилиума

**Полный список посылок вердикта:**
1. Сигнатура из M1 в силе.
2. \`dayPlan\` определён как срез дня — новое, здесь принятое.
`;
  const problems = meetingVerdictProblems(md, []);
  assert.equal(problems.length, 1);
  assert.match(problems[0], /ВЫВОД этой же комнаты/u);
});

test('ловит колонизацию чужих комнат в DoD (hard rule 2)', () => {
  const md = `${head}## Итоговое решение консилиума

**Полный список посылок вердикта:**
1. Сигнатура из M1 в силе.

**Definition of Done:**
- Отчёт несёт highlight-строку по вопросу F с провенансом.
- Тернарный verdict по вопросу H закреплён контракт-тестом.
`;
  const problems = meetingVerdictProblems(md, ['H', 'F', 'T']);
  assert.equal(problems.length, 1);
  assert.match(problems[0], /чужим вопросам \(H, F\)/u);
});

test('ловит отсутствие секции посылок', () => {
  const md = `${head}## Итоговое решение консилиума

Решили: объём — день.
`;
  const problems = meetingVerdictProblems(md, []);
  assert.equal(problems.length, 1);
  assert.match(problems[0], /нет секции «список посылок»/u);
});

test('упоминание «список посылок» в прозе НЕ подменяет секцию (ловушка #558 в самом гейте)', () => {
  // Живой случай M1′: реплика Teamlead несла фразу «Полный список посылок фиксируем
  // явно», и первая версия проверки схватила её вместо настоящей секции ниже —
  // вернув «чисто» на протоколе с реальным дефектом.
  const md = `${head}[Teamlead]: Полный список посылок фиксируем явно, чтобы не повторить домысел.

[Структурщик]: Принимаю.

## Итоговое решение консилиума

**Полный список посылок вердикта (явно):**
1. Сигнатура из M1 в силе.
2. \`dayPlan\` определён как срез дня — новое, здесь принятое.
`;
  const problems = meetingVerdictProblems(md, []);
  assert.equal(problems.length, 1);
  assert.match(problems[0], /ВЫВОД этой же комнаты/u);
});

test('соседний вопрос УПОМЯНУТ вне DoD — не нарушение', () => {
  const md = `${head}[Структурщик]: Вопрос F — сток, здесь его не решаем.

## Итоговое решение консилиума

**Полный список посылок вердикта:**
1. Сигнатура из M1 в силе.

**Definition of Done:**
- Тест без сети.
`;
  assert.deepEqual(meetingVerdictProblems(md, ['H', 'F', 'T']), []);
});
