#!/usr/bin/env node
/**
 * Install Caddy reverse proxy for media.membrana.space → 127.0.0.1:3010.
 * TLS: Let's Encrypt (default) once DNS A-record points to VPS.
 *
 * Usage:
 *   node scripts/_ssh-media-tls-setup.mjs
 *   node scripts/_ssh-media-tls-setup.mjs --check-dns
 *   node scripts/_ssh-media-tls-setup.mjs --manual-cert
 *
 * Env (.env, not committed):
 *   BACKGROUND_MEDIA_IPV4
 *   BACKGROUND_MEDIA_PASSWORD
 *   MEDIA_DOMAIN=media.membrana.space   (optional)
 */
import { readFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { Client } from 'ssh2';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const envText = readFileSync(resolve(root, '.env'), 'utf8');
const get = (key) => envText.match(new RegExp(`^${key}=(.*)$`, 'm'))?.[1]?.trim() ?? '';

const host = get('BACKGROUND_MEDIA_IPV4');
const password = get('BACKGROUND_MEDIA_PASSWORD');
const domain = get('MEDIA_DOMAIN') || 'media.membrana.space';
const mode = process.argv.includes('--check-dns')
  ? 'check-dns'
  : process.argv.includes('--manual-cert')
    ? 'manual-cert'
    : 'setup';

if (!host || !password) {
  console.error('Missing BACKGROUND_MEDIA_IPV4 or BACKGROUND_MEDIA_PASSWORD in .env');
  process.exit(1);
}

const caddyfile = `# @membrana/background-media — generated by scripts/_ssh-media-tls-setup.mjs
${domain} {
\tencode gzip
\treverse_proxy 127.0.0.1:3010
}
`;

const caddyfileManual = `# @membrana/background-media — manual TLS (upload certs to /etc/membrana/ssl/)
${domain} {
\tencode gzip
\ttls /etc/membrana/ssl/fullchain.pem /etc/membrana/ssl/privkey.pem
\treverse_proxy 127.0.0.1:3010
}
`;

const remoteScripts = {
  'check-dns': `#!/bin/bash
set -euo pipefail
echo "=== VPS public IP ==="
curl -fsS https://api.ipify.org && echo ""
echo "=== DNS A for ${domain} ==="
dig +short A ${domain} || getent ahosts ${domain} | awk '{print $1}' | sort -u
echo "=== local health ==="
curl -fsS http://127.0.0.1:3010/health && echo ""
echo "=== caddy status ==="
systemctl is-active caddy 2>/dev/null || echo "caddy: not installed"
`,

  setup: `#!/bin/bash
set -euo pipefail
export DEBIAN_FRONTEND=noninteractive

echo "=== [1/5] install caddy ==="
if ! command -v caddy >/dev/null 2>&1; then
  apt-get update -qq
  apt-get install -y debian-keyring debian-archive-keyring apt-transport-https curl
  curl -1sLf 'https://dl.cloudsmith.io/public/caddy/stable/gpg.key' | gpg --dearmor -o /usr/share/keyrings/caddy-stable-archive-keyring.gpg
  curl -1sLf 'https://dl.cloudsmith.io/public/caddy/stable/debian.deb.txt' | tee /etc/apt/sources.list.d/caddy-stable.list
  apt-get update -qq
  apt-get install -y caddy
else
  echo "caddy already installed: $(caddy version)"
fi

echo "=== [2/5] firewall ==="
if command -v ufw >/dev/null 2>&1; then
  ufw allow 22/tcp || true
  ufw allow 80/tcp || true
  ufw allow 443/tcp || true
  ufw --force enable || true
fi

echo "=== [3/5] Caddyfile ==="
mkdir -p /etc/caddy/Caddyfile.d
cat > /etc/caddy/Caddyfile.d/media.caddy <<'CADDYEOF'
${caddyfile.replaceAll('\t', '  ')}
CADDYEOF

if ! grep -q 'import /etc/caddy/Caddyfile.d' /etc/caddy/Caddyfile 2>/dev/null; then
  echo '' >> /etc/caddy/Caddyfile
  echo 'import /etc/caddy/Caddyfile.d/*' >> /etc/caddy/Caddyfile
fi

echo "=== [4/5] validate & reload ==="
caddy validate --config /etc/caddy/Caddyfile
systemctl enable caddy
systemctl reload caddy || systemctl restart caddy
sleep 3
systemctl --no-pager status caddy | head -20

echo "=== [5/5] checks ==="
echo "DNS A:"
dig +short A ${domain} || true
echo "HTTPS health (may fail until DNS propagates):"
curl -fsS --max-time 10 https://${domain}/health && echo "" || echo "HTTPS not ready yet — wait for DNS, then: systemctl reload caddy"
`,

  'manual-cert': `#!/bin/bash
set -euo pipefail

if [[ ! -f /etc/membrana/ssl/fullchain.pem || ! -f /etc/membrana/ssl/privkey.pem ]]; then
  echo "Missing /etc/membrana/ssl/fullchain.pem or privkey.pem" >&2
  echo "Upload certs first (see docs/deploy/BACKGROUND_MEDIA_DEPLOY.md §5b)" >&2
  exit 1
fi

mkdir -p /etc/caddy/Caddyfile.d
cat > /etc/caddy/Caddyfile.d/media.caddy <<'CADDYEOF'
${caddyfileManual.replaceAll('\t', '  ')}
CADDYEOF

if ! grep -q 'import /etc/caddy/Caddyfile.d' /etc/caddy/Caddyfile 2>/dev/null; then
  echo '' >> /etc/caddy/Caddyfile
  echo 'import /etc/caddy/Caddyfile.d/*' >> /etc/caddy/Caddyfile
fi

caddy validate --config /etc/caddy/Caddyfile
systemctl reload caddy || systemctl restart caddy
sleep 2
curl -fsS --max-time 10 https://${domain}/health && echo ""
`,
};

function runRemote(script) {
  return new Promise((resolvePromise, rejectPromise) => {
    const conn = new Client();
    const timeout = setTimeout(() => {
      conn.end();
      rejectPromise(new Error('SSH timeout (10m)'));
    }, 10 * 60 * 1000);

    conn
      .on('ready', () => {
        conn.exec('bash -s', (err, stream) => {
          if (err) {
            clearTimeout(timeout);
            conn.end();
            rejectPromise(err);
            return;
          }
          stream.write(script);
          stream.end();
          stream.on('data', (d) => process.stdout.write(d));
          stream.stderr.on('data', (d) => process.stderr.write(d));
          stream.on('close', (code) => {
            clearTimeout(timeout);
            conn.end();
            if (code === 0) resolvePromise(code);
            else rejectPromise(new Error(`remote exit ${code}`));
          });
        });
      })
      .on('error', rejectPromise)
      .connect({ host, port: 22, username: 'root', password, readyTimeout: 20000 });
  });
}

console.log(`TLS setup (${mode}) on root@${host} for ${domain}\n`);
await runRemote(remoteScripts[mode]);
console.log('\nDone.');
