/**
 * consilium-premises — принудительный шаг посылок для режима `--meeting`.
 *
 * Доказательная база: заседание `procedural-layer` 21.07 — ВОСЕМЬ движковых
 * отказов гейта `meetingVerdictProblems` подряд (черновики в
 * docs/seanses/rejected/procedural-layer-*). Комнаты стохастически не пишут
 * секцию «Список посылок», независимо от силы формулировки в повестке и задании
 * (M1/M2 прошли со 2-й попытки, M3 — 0 из 6). Решение владельца 21.07: фикс
 * движка отдельным блоком работ.
 *
 * Конструкция (вердикт M5: прецеденты в контейнере процедуры meeting):
 * 1. **Шаг посылок** — если единственный дефект вердикта «нет секции посылок»,
 *    движок дозапрашивает секцию ДОПОЛНИТЕЛЬНЫМ ХОДОМ ТОЙ ЖЕ КОМНАТЫ (тот же
 *    прогон, контекст диалога сохранён: prompt → протокол → просьба огласить).
 *    Это НЕ «посылки задним числом»: запрет M2″ 18.07 бьёт по ЧУЖОЙ комнате,
 *    называющей посылки чужого вердикта; здесь комната договаривает свой.
 * 2. **Авто-перепрогон** — если шаг посылок не помог или дефекты иные
 *    (вывод в посылках, колонизация DoD) — одна полная итерация с явным
 *    предупреждением о провале предыдущей комнаты; дальше — честный отказ.
 */

import { MISSING_PREMISES_PROBLEM, PREMISES_SECTION_TITLE } from './protocol-validator.mjs';

/** Заголовок секции, который дозапрашиваем, — ровно тот, что проверяет гейт. */
export const PREMISES_HEADER = `## ${PREMISES_SECTION_TITLE}`;

/**
 * Дефект «нет секции посылок» — единственный, который лечится дозапросом секции.
 * Прочие дефекты (вывод в посылках, колонизация) требуют пересмотра вердикта,
 * а не дописывания — их лечит только перепрогон.
 *
 * @param {string[]} problems из meetingVerdictProblems
 * @returns {boolean} true ⟺ problems = ровно один дефект отсутствия секции
 */
export function onlyMissingPremises(problems) {
  return problems.length === 1 && problems[0] === MISSING_PREMISES_PROBLEM;
}

/**
 * Промпт дозапроса секции у той же комнаты (второй ход того же прогона).
 * @returns {string}
 */
export function buildPremisesRepairPrompt() {
  return [
    `Комната, протокол выше не несёт секцию «${PREMISES_SECTION_TITLE}» — гейт отклонит его целиком.`,
    'Догово́рите свой вердикт: огласите посылки, НА КОТОРЫХ ОН УЖЕ СТОИТ.',
    '',
    'Требования (машинный гейт):',
    `- ответ начинается строкой заголовка ровно «${PREMISES_HEADER}»;`,
    '- до 10 позиций, каждая одной строкой, у каждой пометка **факт** (проверяемо по коду/замеру) или **норма** (наше решение);',
    '- ТОЛЬКО входы, использованные в рассуждении выше; выводы этой же комнаты вносить нельзя;',
    '- ничего, кроме секции: без преамбулы, без повторения вердикта.',
  ].join('\n');
}

/**
 * Вставить секцию посылок в протокол: перед футером «*Реплик в диалоге…*»,
 * если он есть, иначе — в конец. Снипет нормализуется: гарантируется заголовок
 * ровно PREMISES_HEADER (модель порой добавляет преамбулу — срезаем всё до него;
 * если заголовка нет вовсе — добавляем над содержимым).
 *
 * @param {string} protocolMd протокол без секции посылок
 * @param {string} premisesSnippet ответ комнаты на дозапрос
 * @returns {string} протокол с секцией
 */
export function insertPremisesSection(protocolMd, premisesSnippet) {
  let section = String(premisesSnippet ?? '').trim();
  const headerAt = section.indexOf(PREMISES_HEADER);
  if (headerAt >= 0) section = section.slice(headerAt);
  else section = `${PREMISES_HEADER}\n\n${section}`;

  // Пустой ответ дозапроса (заголовок без тела) вставлять нельзя: тело секции
  // слиплось бы со следующим текстом и гейт дал бы ложную зелёнку (ревью 21.07,
  // P2 подтверждён тестом). Не вставляем — гейт остаётся красным честно, дальше
  // сработает авто-перепрогон.
  const body = section.slice(PREMISES_HEADER.length).trim();
  if (body === '') return String(protocolMd);

  const lines = String(protocolMd).split(/\r?\n/);
  // Футер реплик — последняя служебная строка вида `*Реплик в диалоге: N…*`.
  const footerIdx = lines.findIndex((l) => /^\s*\*Реплик в диалоге:/u.test(l));
  if (footerIdx === -1) {
    return `${protocolMd.replace(/\s+$/u, '')}\n\n${section}\n`;
  }
  const before = lines.slice(0, footerIdx).join('\n').replace(/\s+$/u, '');
  const after = lines.slice(footerIdx).join('\n');
  return `${before}\n\n${section}\n\n${after}`;
}

/**
 * Блок предупреждения для авто-перепрогона (одна итерация).
 * @param {string[]} problems дефекты предыдущей комнаты
 * @returns {string}
 */
export function buildRerunNotice(problems) {
  return [
    '---',
    '## АВТО-ПЕРЕПРОГОН — предыдущая комната провалила машинный гейт',
    '',
    ...problems.map((p) => `- ✖ ${p}`),
    '',
    `Вердикт без секции «${PREMISES_SECTION_TITLE}» (входы с пометками факт/норма,`,
    'названные В ЭТОМ прогоне) отклоняется целиком. Не повторите провал.',
    '',
  ].join('\n');
}
