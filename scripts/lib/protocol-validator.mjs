/**
 * protocol-validator (#469 ti-2): валидатор канона протокола консилиума /
 * инсайт-ревью для оффлайн-канала (когда LLM недоступен, протокол пишется в
 * IDE-чате). Чистые функции — только парсинг и арифметика, без сети и LLM.
 *
 * Роли Membrana (метки в квадратных скобках). Реплика роли начинается со
 * строки `[Роль]:` или `[Роль — Имя]:` (формат competition-протоколов).
 */
export const MEMBRANA_ROLE_LABELS = ['Teamlead', 'Структурщик', 'Математик', 'Музыкант', 'Верстальщик'];

/** Число реплик каждой роли: `[Роль]:` / `[Роль — Имя]:` в начале строки. */
export function countRoleReplies(md, roleLabels = MEMBRANA_ROLE_LABELS) {
  const counts = Object.fromEntries(roleLabels.map((r) => [r, 0]));
  for (const line of md.split(/\r?\n/)) {
    // `(?:\*\*)?` — жирная метка `**[Роль]:**` / `**[Роль]**:` легальна (mirrors REPLY_START_RE, #1046):
    // без поблажки countRoleReplies возвращал 0 по всем ролям на жирных протоколах → ложный «0» в футере.
    const m = line.match(/^\s*(?:\*\*)?\[([^\]:]+?)(?:\s+—[^\]]*)?\](?:\*\*)?\s*:/u);
    if (!m) continue;
    const role = m[1].trim();
    if (role in counts) counts[role] += 1;
  }
  return counts;
}

/**
 * Сверить прозаический футер «Реплик в диалоге: N» с фактическим числом реплик
 * и исправить N, если модель ошиблась. Возвращает исправленный md + метаданные.
 *
 * Трение 17.07: футер M0 говорил «21» при фактических 20 (поймал аудитор).
 * Число в футере пишет МОДЕЛЬ и может соврать; фактическое — детерминируемо
 * (`countRoleReplies`). Приводим прозу к факту, чтобы артефакт не нёс ложь.
 * Если футера нет — md не трогаем (corrected:false, matched:false).
 */
export function reconcileReplyCount(md, roleLabels = MEMBRANA_ROLE_LABELS) {
  const counts = countRoleReplies(md, roleLabels);
  const total = Object.values(counts).reduce((a, b) => a + b, 0);
  const re = /(Реплик в диалоге:\s*)(\d+)/u;
  const m = md.match(re);
  if (!m) return { md, total, matched: false, corrected: false, stated: null };
  const stated = Number(m[2]);
  const corrected = stated !== total;
  const out = corrected ? md.replace(re, `$1${total}`) : md;
  return { md: out, total, matched: true, corrected, stated };
}

/**
 * Метки `[…]:` в начале строк, НЕ совпавшие ни с одной известной ролью.
 *
 * rt-6: инцидент 16.07 — протокол пришёл в формате `[Имя · Роль]:` вместо
 * `[Роль — Имя]:`. countRoleReplies никого не распознал → «роль не высказалась»
 * ×5, что уводило от корня (роли ГОВОРИЛИ, формат метки другой). Эта функция даёт
 * гейту материал для внятной подсказки вместо ложного «все молчат».
 */
export function unknownBracketLabels(md, roleLabels = MEMBRANA_ROLE_LABELS) {
  const known = new Set(roleLabels);
  const out = [];
  for (const line of md.split(/\r?\n/)) {
    // `(?:\*\*)?` — жирная метка тоже легальна (mirrors REPLY_START_RE, #1046): иначе `**[Имя · Роль]:**`
    // не матчится вовсе — гейт молчит вместо внятной подсказки о неизвестной форме метки.
    const m = line.match(/^\s*(?:\*\*)?\[([^\]]+?)\](?:\*\*)?\s*:/u);
    if (!m) continue;
    const label = m[1].trim();
    // `[Роль — Имя]` — известная форма: сверяем ЛЕВУЮ часть до тире.
    const roleHead = label.split(/\s+—/u)[0].trim();
    if (!known.has(roleHead) && !out.includes(label)) out.push(label);
  }
  return out;
}

/**
 * Фазовые метки, ЗАРЕЗЕРВИРОВАННЫЕ под номер фазы и потому не являющиеся ID вопроса:
 * `M0…Mn` — фазы заседания (MEETING_REGULATION), `NB0…NBn` — кванты ночи
 * (NIGHT_SPRINT_REGULATION).
 *
 * #639: обе жили в прозе повестки жирным («порядок из M0 → **M2 = кандидат 2**»,
 * «ночь дала три кванта: **NB1**, **NB6**, **NB5**») и попадали в ID-вопросы — S-M1
 * дважды отказывал до прогона, сообщая «повестка несёт 2/4 вопроса», хотя вопрос был
 * один. Следствие резервирования: `M<цифры>` и `NB<цифры>` НЕЛЬЗЯ использовать как ID
 * вопроса повестки. За всю историю вопросы шли `O1`, `A1`, `B1`, `T1`, `Q1`…`Q6`,
 * `C1` — пересечения с фазовыми метками нет.
 */
export const RESERVED_PHASE_LABEL = /^(?:M|NB)\d+$/u;

/**
 * ID вопросов повестки из topic-файла: жирные метки `**A1`, `**B2`, `**Q3`.
 *
 * Конвенция rt-6: вопрос повестки помечается ID вида «буква(ы)+цифры», жирным.
 * Сегодня повестки уже так и размечались (A1/A2/A3, B1–B3, C1, Q1–Q6).
 *
 * Фазовые метки (`M2`, `NB1`) отбрасываются — см. RESERVED_PHASE_LABEL. Сузить захват
 * до секции «единственный вопрос» НЕЛЬЗЯ: та же функция кормит `findUncoveredAgendaItems`
 * (rt-6) на ОБЫЧНЫХ многовопросных повестках консилиума — сужение сломало бы покрытие.
 */
export function extractAgendaIds(topicMd) {
  const ids = [];
  for (const m of String(topicMd ?? '').matchAll(/\*\*([A-ZА-Я]{1,2}\d+)\b/gu)) {
    if (RESERVED_PHASE_LABEL.test(m[1])) continue;
    if (!ids.includes(m[1])) ids.push(m[1]);
  }
  return ids;
}

/**
 * Начало реплики роли: `[Роль]:`, `**[Роль]:**`, `**[Роль]**:`.
 *
 * `**` вокруг метки — легальный markdown и живая практика ручных протоколов: заседание
 * `tasks-workshop` (23.07) писалось `**[Структурщик]:**`. Регексп без этой поблажки не
 * находил ни одной реплики, `protocolBody` возвращал пустую строку — и ВСЕ проверки,
 * работающие по телу (`hasVerdictSection`, `premisesSection`, `findUncoveredAgendaItems`,
 * `meetingVerdictProblems`), давали ложный красный: шесть нарушений на здоровых файлах
 * (#1046). Ложный красный на легальной записи обесценивает аудитора сильнее, чем
 * пропущенный дефект: следующий агент чинит несуществующее или перестаёт верить гейту.
 */
const REPLY_START_RE = /^\s*(?:\*\*)?\[[^\]]+\](?:\*\*)?\s*:/u;

/**
 * Тело протокола — реплики и итог, БЕЗ эхо-заголовка с вопросом.
 *
 * Ключ детектора: ID повестки встречаются в эхе `**Вопрос:**` (его вставляет сам
 * прогон), и наивный grep дал бы ложное «покрыто». Реальное покрытие — только если
 * ID назван в РЕПЛИКЕ или ИТОГЕ. Отсекаем всё до первой реплики `[Роль]:`.
 */
export function protocolBody(protocolMd) {
  const lines = String(protocolMd ?? '').split(/\r?\n/);
  const firstReply = lines.findIndex((l) => REPLY_START_RE.test(l));
  return firstReply === -1 ? '' : lines.slice(firstReply).join('\n');
}

/**
 * Вопросы повестки, НЕ покрытые протоколом (ID есть в topic, нет в теле протокола).
 *
 * Ровно тот сбой, что повторился ТРИЖДЫ за 16.07: консилиум расходился на первых
 * вопросах и молча ронял остальные. Здесь он становится машинным вердиктом.
 */
export function findUncoveredAgendaItems(topicMd, protocolMd) {
  const ids = extractAgendaIds(topicMd);
  const body = protocolBody(protocolMd);
  return ids.filter((id) => !new RegExp(`\\b${id}\\b`, 'u').test(body));
}

/**
 * Есть ли в протоколе секция вердикта/итогового решения.
 *
 * NB3 (#558): findUncoveredAgendaItems грепает МЕТКУ, а не вердикт — не отличает
 * «вопрос уронен» от «отвечен под другим заголовком без ID-метки» (17.07: консилиум
 * ВИЗОР дал итоговую таблицу без меток V1..V4, rt-6 соврал «не покрыта»). Семантику
 * машина не проверит, но НАЛИЧИЕ секции вердикта — да; это разводит два случая:
 * метки нет + вердикта нет = вероятно уронено; метки нет + вердикт есть = вероятно
 * отвечено без метки. Честный сигнал вместо ложноавторитетного «уронено».
 */
export function hasVerdictSection(protocolMd) {
  const body = protocolBody(protocolMd);
  // `\p{L}`, не `\w`: в JS `\w` это [A-Za-z0-9_] и кириллицу НЕ матчит, поэтому ветка
  // `итогов\w*\s+решени` была мертва — обычный заголовок «## Итоговое решение» не
  // распознавался, проходил только частный случай «…консилиума» из второй альтернативы.
  // Пока функция лишь уточняла формулировку сообщения, дефект был невиден; с #619 она
  // решает, ронять прогон или нет, и молчаливый false стоил бы ложного красного.
  return /^\s{0,3}#{1,4}\s.*(итогов\p{L}*\s+решени|вердикт|решени\p{L}*\s+консилиум|definition of done)/imu.test(body)
    || /итоговое решение консилиума/iu.test(body);
}

/**
 * Нарушение повестки ЗАСЕДАНИЯ: она обязана нести ровно ОДИН ID-вопрос (S-M1).
 *
 * Регламент: `docs/MEETING_REGULATION.md`. Почему отказ, а не предупреждение —
 * три ступени эскалации: промпт-дисциплина (урок 11.07 «пронумеруй точки, требуй эхо
 * с вердиктом») 17.07 была ИСПОЛНЕНА и всё равно не удержала; детекция
 * `findUncoveredAgendaItems` ловит уже по сожжённому прогону. При одном вопросе ронять
 * нечего: вердикт либо есть, либо заседания не было (S-M2).
 *
 * M0 (установочное) — не исключение: его единственный вопрос «какой порядок
 * зависимостей?», а кандидаты лежат в теле простым текстом без жирных ID и сюда не
 * попадают. Поэтому правило единообразно и флаг-исключение не нужен.
 *
 * Ноль вопросов — тоже нарушение: вердикту не на чем встать.
 *
 * @returns {string} пустая строка = повестка годна для заседания
 */
export function meetingAgendaProblem(topicMd) {
  const ids = extractAgendaIds(topicMd);
  if (ids.length === 0) return 'повестка без ID-вопроса — вердикту не на чем встать';
  if (ids.length > 1) {
    return `повестка несёт ${ids.length} вопрос(ов) (${ids.join(', ')}) — заседание разбирает ровно один`;
  }
  // Повестка обязана ТРЕБОВАТЬ посылки. Добор задним числом провалился (M2″ 18.07:
  // комната назвала посылки ЧУЖОГО вердикта) — значит основание называется в тот же
  // прогон, что и вердикт, либо не называется никогда. Отказ до прогона дешевле.
  if (!/список\s+посылок|посылк\w*\s+обязат/iu.test(String(topicMd ?? ''))) {
    return 'повестка не требует списка посылок — вердикт встанет на неназванном фундаменте';
  }
  return '';
}

/**
 * Маркеры вывода, попавшего в список ПОСЫЛОК.
 *
 * Посылка — это ВХОД (⊆ закрытые вердикты предшественников). Заключение самой комнаты
 * входом быть не может: тест на отзыв на нём не запускается — отозвать такую «посылку»
 * значит отозвать сам вердикт. Смешение делает список посылок непроверяемым.
 */
const CONCLUSION_IN_PREMISE_MARKERS =
  /(новое,?\s+здесь\s+принят|здесь\s+принят|принято\s+здесь|решено\s+здесь|вывод\s+эт(ой|ого)\s+комнат)/iu;

/**
 * Канонический заголовок секции посылок — ЕДИНЫЙ источник для гейта и промпта.
 *
 * Гейт (`premisesSection` ниже) ищет этот заголовок; режим `--meeting` в
 * `consilium.mjs` его же кладёт в промпт требованием формата. Держать в одном месте
 * обязательно: 19.07 три прогона M0 заседания `team-execution-contour` подряд ушли в
 * `rejected/` именно на расхождении формулировок — регламент говорил «секция посылок»,
 * гейт требовал слова «список посылок», а комната писала `**Посылки:**` и не знала о
 * требовании вовсе, потому что промпт о нём молчал. Правило 6 имело зубы на выходе и
 * не имело голоса на входе.
 */
export const PREMISES_SECTION_TITLE = 'Список посылок';

/**
 * Текст дефекта «нет секции посылок» — экспортирован константой, чтобы
 * потребители (шаг посылок в consilium) матчили по значению, а не по хрупкой
 * подстроке (ревью 21.07, P2).
 */
export const MISSING_PREMISES_PROBLEM =
  'нет секции «список посылок» — вердикт не называет, на чём стоит';

/** Заголовок секции посылок — регексп собран из {@link PREMISES_SECTION_TITLE}. */
const PREMISES_HEADING_RE = new RegExp(PREMISES_SECTION_TITLE.replace(/\s+/gu, '\\s+'), 'iu');

/**
 * Тело секции посылок вердикта.
 *
 * ЗАГОЛОВКОМ считается только строка-заголовок (`**…**` или `#…`), НЕ упоминание в
 * прозе. Первый прогон этой проверки поймал сам себя: в протоколе M1′ реплика Teamlead
 * несла фразу «Полный список посылок фиксируем явно», регексп схватил её вместо
 * настоящей секции ниже — и вернул «чисто» на протоколе с реальным дефектом. Это тот
 * же класс #558 (упоминание вместо предмета), только уже в гейте против #558.
 */
function premisesSection(protocolMd) {
  const lines = protocolBody(protocolMd).split(/\r?\n/);
  const isHeader = (l) =>
    /^\s{0,3}(?:\*\*|#{1,4}\s)/u.test(l) && PREMISES_HEADING_RE.test(l);
  const start = lines.findIndex(isHeader);
  if (start === -1) return '';
  // Секция кончается следующим заголовком любого рода.
  const rest = lines.slice(start + 1);
  const end = rest.findIndex((l) => /^\s{0,3}(?:\*\*|#{1,4}\s)/u.test(l));
  return (end === -1 ? rest : rest.slice(0, end)).join('\n');
}

/**
 * Структурные дефекты вердикта ЗАСЕДАНИЯ (проверка 4 аудитора, с зубами).
 *
 * Ловит два рецидива, задокументированных на `meeting-evening-auditor` 18.07 — оба
 * прошли мимо человека дважды подряд:
 *
 * 1. **Вывод в списке посылок.** M1′ внёс «`dayPlan` определён как срез дня — новое,
 *    здесь принятое» в посылки. Это заключение комнаты, а не вход; список посылок
 *    перестаёт быть проверяемым тестом на отзыв.
 * 2. **Колонизация чужих комнат.** M1 вынес обязывающие положения по H и F, M1′ — по
 *    F и T, хотя те не созывались. Ловим по упоминанию ID соседних вопросов в секции
 *    DoD: DoD — это обязательства СВОЕЙ комнаты.
 *
 * Почему не проза: урок 11.07 («пронумеруй точки») был ИСПОЛНЕН и не удержал; аудит
 * назвал дефект дважды — и он воспроизвёлся через 16 минут. Прозой класс не держится.
 *
 * ЧЕСТНЫЕ ПРЕДЕЛЫ (не зелёнка):
 * - Колонизация ловится ТОЛЬКО по явному упоминанию ID соседа в DoD. M1′ колонизировал
 *   F и T, ни разу их не назвав (highlight-строка, провенанс, «человек утром»), — такую
 *   проверка НЕ увидит. Семантику машина не судит; это работа аудитора-агента.
 * - «Спрятанная посылка» (несущий вход, не внесённый в список) машинно не ловится
 *   вовсе: чтобы знать, что вход пропущен, надо понимать вывод.
 * Пустой результат означает «структурных дефектов не найдено», НЕ «вердикт чист».
 *
 * @param {string} protocolMd протокол консилиума
 * @param {string[]} siblingIds ID соседних вопросов заседания (чужие комнаты)
 * @returns {string[]} список нарушений; пусто = структура вердикта годна
 */
export function meetingVerdictProblems(protocolMd, siblingIds = []) {
  const problems = [];
  const body = protocolBody(protocolMd);

  const premises = premisesSection(protocolMd);
  if (premises === '') {
    problems.push(MISSING_PREMISES_PROBLEM);
  } else if (CONCLUSION_IN_PREMISE_MARKERS.test(premises)) {
    problems.push(
      'в списке посылок есть ВЫВОД этой же комнаты (маркер «новое, здесь принятое») — ' +
        'посылка обязана быть входом, иначе тест на отзыв не запускается',
    );
  }

  // DoD — обязательства своей комнаты; ID соседей там = колонизация (hard rule 2).
  const dod = body.match(/Definition of Done([\s\S]*?)(?=\n\s{0,3}#{1,4}\s|$)/iu)?.[1] ?? '';
  const colonized = siblingIds.filter((id) => new RegExp(`\\b${id}\\b`, 'u').test(dod));
  if (colonized.length > 0) {
    problems.push(
      `DoD несёт обязательства по чужим вопросам (${colonized.join(', ')}) — ` +
        'hard rule 2: смежный вопрос упомянуть можно, вердикт по нему выносить нельзя',
    );
  }

  return problems;
}


/** Таблица «Роль | … | /10» инсайт-ревью → {scores[], average, declared}. */
export function parseVotingTable(md) {
  const scores = [];
  for (const line of md.split(/\r?\n/)) {
    const cells = line.split('|').map((c) => c.trim());
    if (cells.length < 3) continue;
    const label = cells[1];
    const last = cells[cells.length - 2]; // последняя непустая колонка (перед хвостовым |)
    if (!MEMBRANA_ROLE_LABELS.includes(label)) continue;
    const n = Number(last);
    if (Number.isFinite(n) && n >= 1 && n <= 10) scores.push(n);
  }
  const declared = md.match(/\*\*Средний балл(?:\s+команды)?:\*\*\s*([\d.]+)/)?.[1];
  const average = scores.length ? scores.reduce((a, b) => a + b, 0) / scores.length : null;
  return { scores, average, declared: declared != null ? Number(declared) : null };
}

/**
 * Общий валидатор канона. kind: 'consilium' (≥ minReplies суммарно, каждая роль
 * ≥1, секция итога) | 'insight-review' (5 ролей ≥1, таблица голосования, средний
 * балл сходится с заявленным ±0.1).
 *
 * `notes` — наблюдения БЕЗ права отказа (#619). Отдельный канал нужен затем, чтобы
 * слабый сигнал не подмешивался к `problems` и не ронял прогон: смешение этих двух
 * и дало три ложных красных на заседании 18.07.
 *
 * `meeting` (#616, вариант A) — протокол ЗАСЕДАНИЯ: к канону добавляются структурные
 * проверки вердикта, и «нет секции посылок» становится ОТКАЗОМ, а не замечанием
 * постфактум. Почему при записи: комната устроена выносить суждение, а не
 * реконструировать основание — спрошенная о посылках после прогона, она их
 * правдоподобно сочиняет (M2″ 18.07: 8 уверенных посылок ЧУЖОГО вердикта). Момент,
 * когда основание ещё можно назвать честно, — сам прогон; поэтому гейт обязан стоять
 * до появления вердикта в файле, а не после.
 *
 * @returns {{ ok: boolean, problems: string[], notes: string[], stats: object }}
 */
export function validateProtocol(
  md,
  {
    kind = 'consilium',
    minReplies = 20,
    roleLabels = MEMBRANA_ROLE_LABELS,
    agenda = null,
    meeting = false,
    siblingIds = [],
  } = {},
) {
  const problems = [];
  const notes = [];
  const counts = countRoleReplies(md, roleLabels);
  const total = Object.values(counts).reduce((a, b) => a + b, 0);

  // (3) каждая роль ≥1 реплики (обе формы протокола)
  const silent = Object.entries(counts).filter(([, n]) => n === 0);
  const unknown = unknownBracketLabels(md, roleLabels);
  for (const [role] of silent) {
    problems.push(`роль «${role}» не высказалась ни разу`);
  }
  // rt-6 подсказка: если роли «молчат», но есть нераспознанные метки — корень в
  // ФОРМАТЕ, а не в молчании (инцидент 16.07: `[Имя · Роль]` вместо `[Роль — Имя]`).
  if (silent.length > 0 && unknown.length > 0) {
    problems.push(
      `возможно, формат меток неверный — не распознаны: ${unknown.slice(0, 3).map((l) => `[${l}]`).join(', ')}. ` +
        'Канон: `[Роль — Имя]:` в начале строки',
    );
  }

  // rt-6 полнота повестки: каждый ID вопроса обязан быть назван в реплике/итоге.
  // Ловит сбой, повторившийся трижды 16.07 — консилиум молча ронял часть повестки.
  //
  // #619: сама по себе метка — не вердикт. На заседании `evening-auditor` (18.07) три
  // прогона из пяти дали ЛОЖНЫЙ КРАСНЫЙ: M0, M1′, M4 вынесли полный вердикт, но без
  // ID-метки в теле — и rt-6 объявил повестку неразобранной. Поэтому решение «ронять»
  // принимает не наличие метки, а связка с `hasVerdictSection`:
  //   метки нет + вердикта нет  → вопрос вероятно уронен, это отказ (зубы сохранены);
  //   метки нет + вердикт есть  → вероятно отвечено без метки, это ЗАМЕТКА, не отказ.
  // Обратный режим (метка есть, существо не разобрано — ложный зелёный M1) машине не
  // виден в принципе; его ловит структурная проверка `meetingVerdictProblems` (правило 6),
  // независимая от меток. Эрратум к S-M2 в docs/MEETING_REGULATION.md это фиксирует.
  if (agenda) {
    const uncovered = findUncoveredAgendaItems(agenda, md);
    if (uncovered.length > 0) {
      const line = `повестка не покрыта: ${uncovered.join(', ')} — ни одной реплики/строки итога`;
      if (hasVerdictSection(md)) {
        notes.push(`${line}. Секция вердикта есть → вероятно отвечено без ID-метки (не отказ, проверить глазами)`);
      } else {
        problems.push(`${line}, и секции вердикта нет — вопрос вероятно уронен`);
      }
    }
  }

  if (kind === 'insight-review') {
    const vote = parseVotingTable(md);
    if (vote.scores.length < roleLabels.length) {
      problems.push(`таблица голосования: распознано ${vote.scores.length} из ${roleLabels.length} ролей`);
    }
    if (vote.declared == null) {
      problems.push('нет строки «**Средний балл:**»');
    } else if (vote.average != null && Math.abs(vote.average - vote.declared) > 0.1) {
      problems.push(`средний балл не сходится: посчитано ${vote.average.toFixed(2)}, заявлено ${vote.declared}`);
    }
    if (!/рекомендуемый статус/iu.test(md)) {
      problems.push('нет резюме с «Рекомендуемый статус»');
    }
    return { ok: problems.length === 0, problems, notes, stats: { counts, total, vote } };
  }

  // consilium
  // (1) метаданные-таблица
  if (!/^\|\s*Поле\s*\|/mu.test(md) && !/^#\s*Метаданные/mu.test(md)) {
    problems.push('нет таблицы метаданных (| Поле | Значение |)');
  }
  // (2) суммарный порог реплик
  if (total < minReplies) {
    problems.push(`реплик ролей ${total} < минимума ${minReplies}`);
  }
  // (4) секция итога/консенсуса
  if (!/итоговое решение|консенсус|## итог/iu.test(md)) {
    problems.push('нет секции итогового решения / консенсуса');
  }

  // (5) заседание: структура вердикта с зубами — посылки обязательны ДО записи (#616).
  if (meeting) {
    problems.push(...meetingVerdictProblems(md, siblingIds));
  }

  return { ok: problems.length === 0, problems, notes, stats: { counts, total } };
}
