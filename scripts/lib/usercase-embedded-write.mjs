import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

import { repoRootFromScripts, resolveUserCaseDir } from './usercase-paths.mjs';

/**
 * @param {string} embeddedRelativePath
 * @param {Record<string, unknown>} document
 * @param {string} [repoRoot]
 */
export function writeEmbeddedDeviceScenarioDocument(
  embeddedRelativePath,
  document,
  repoRoot = repoRootFromScripts(),
) {
  const abs = join(repoRoot, embeddedRelativePath);
  const exportName = abs.includes('mvp-microphone')
    ? 'DEFAULT_USERCASE_MVP_MICROPHONE_DOCUMENT'
    : 'DEFAULT_USERCASE_DOCUMENT';
  const body = JSON.stringify(document, null, 2);
  writeFileSync(
    abs,
    `/**
 * AUTO-GENERATED by yarn usercase:build — do not edit.
 */
import type { DeviceScenarioDocument } from '@membrana/core';

export const ${exportName} = ${body} as unknown as DeviceScenarioDocument;
`,
    'utf8',
  );
}

/**
 * @param {string} rawId
 * @param {Record<string, unknown>} document
 * @param {string} [repoRoot]
 */
export function syncUserCaseBranchBundles(rawId, document, repoRoot = repoRootFromScripts()) {
  const dir = resolveUserCaseDir(rawId, repoRoot);
  const manifest = JSON.parse(readFileSync(`${dir}/manifest.json`, 'utf8'));
  const scenario = document['scenario'];
  if (scenario === undefined || typeof scenario !== 'object') {
    throw new Error('Document missing scenario');
  }

  /** @type {Record<string, unknown>} */
  const branchSubgraphs = {
    onConnect: scenario['onConnect'],
    initial: scenario['initial'],
    main: scenario['loops']?.['main'],
    alarm: scenario['loops']?.['alarm'],
    onStop: scenario['triggers']?.['onStop'],
    onDisconnect: scenario['triggers']?.['onDisconnect'],
  };

  for (const [branch, meta] of Object.entries(manifest.branches ?? {})) {
    const bundleFile = meta['bundleFile'];
    const subgraph = branchSubgraphs[branch];
    if (typeof bundleFile !== 'string' || subgraph === undefined) {
      continue;
    }
    const bundlePath = join(dir, bundleFile);
    const existing = JSON.parse(readFileSync(bundlePath, 'utf8'));
    writeFileSync(
      bundlePath,
      `${JSON.stringify(
        {
          ...existing,
          subgraph,
          exportedAt: new Date().toISOString(),
        },
        null,
        2,
      )}\n`,
      'utf8',
    );

    const legacyFile = meta['legacyFile'];
    if (typeof legacyFile === 'string') {
      const legacyPath = join(repoRoot, 'docs/device-board-scripts', legacyFile);
      writeFileSync(
        legacyPath,
        `${JSON.stringify(
          {
            ...JSON.parse(readFileSync(legacyPath, 'utf8')),
            subgraph,
            exportedAt: new Date().toISOString(),
          },
          null,
          2,
        )}\n`,
        'utf8',
      );
    }
  }
}
